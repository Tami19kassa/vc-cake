// Helper function to extract text containing JSON translations from the database
export const getTranslation = (dbField, currentLang) => {
  if (!dbField) return "";
  try {
    const parsed = JSON.parse(dbField);
    if (parsed && typeof parsed === "object") {
      return parsed[currentLang] || parsed["en"] || "";
    }
  } catch (e) {
    // If it's not a JSON string, it's raw text (fallback to raw field value)
    return dbField;
  }
  return dbField;
};
